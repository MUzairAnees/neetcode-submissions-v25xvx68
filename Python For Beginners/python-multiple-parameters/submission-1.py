def two_sum(num_one, num_two):
    sum_of_nums = num_one + num_two
    print(sum_of_nums)

def three_sum(num_one, num_two, num_three):
    sum_of_nums = num_one + num_two + num_three
    print(sum_of_nums)

two_sum(7, 10)
three_sum(3, 5, 8)

